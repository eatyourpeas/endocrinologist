    required this.percentile50th,
    required this.percentile75th,
    required this.percentile90th,
    required this.percentile95th,
    required this.percentile97th,
  });

  // Optional: A factory constructor to parse from a CSV row (String)
  factory IndianStretchedPenileLength.fromCsvRow(List<String> row) {
    return IndianStretchedPenileLength(
      ageYears: int.parse(row[0]),
      sampleSizeN: int.parse(row[1]),
      weightRange: row[2],
      mean: double.parse(row[3]),
      sd: double.parse(row[4]),
      median: double.parse(row[5]),
      min: double.parse(row[6]),
      max: double.parse(row[7]),
      m: double.parse(row[8]),
      s: double.parse(row[9]),
      l: double.parse(row[10]),
      t: double.parse(row[11]),
      percentile3rd: double.parse(row[12]),
      percentile5th: double.parse(row[13]),
      percentile10th: double.parse(row[14]),
      percentile25th: double.parse(row[15]),
      percentile50th: double.parse(row[16]),
      percentile75th: double.parse(row[17]),
      percentile90th: double.parse(row[18]),
      percentile95th: double.parse(row[19]),
      percentile97th: double.parse(row[20]),
    );
  }

  @override
  String toString() {
    return 'IndianStretchedPenileLength(Age: $ageYears, Sample Size: $sampleSizeN, Mean: $mean, Median: $median, Max: $max)';
  }
}

